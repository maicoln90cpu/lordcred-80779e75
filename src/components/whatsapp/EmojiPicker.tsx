interface EmojiPickerProps {
  onSelect: (emoji: string) => void;
  disabled?: boolean;
}

export default function EmojiPicker({ onSelect, disabled }: EmojiPickerProps) {
  const [open, setOpen] = useState(false);
  const [activeCategory, setActiveCategory] = useState(0);

  const handleSelect = (emoji: string) => {
    onSelect(emoji);
  };

  return (
    <Popover open={open} onOpenChange={setOpen}>
      <PopoverTrigger asChild>
        <Button variant="ghost" size="icon" className="text-muted-foreground hover:text-foreground shrink-0" disabled={disabled}>
          <Smile className="w-5 h-5" />
        </Button>
      </PopoverTrigger>
      <PopoverContent side="top" align="start" className="w-80 p-0">
        {/* Category tabs */}
        <div className="flex border-b border-border px-1 pt-1 gap-0.5 overflow-x-auto">
          {CATEGORIES.map((cat, i) => (
            <button
              key={cat.name}
              onClick={() => setActiveCategory(i)}
              className={cn(
                "px-2 py-1.5 text-base rounded-t-md transition-colors shrink-0",
                activeCategory === i ? "bg-secondary" : "hover:bg-secondary/50"
              )}
              title={cat.name}
            >
              {cat.label}
            </button>
          ))}
        </div>
        {/* Emoji grid */}
        <div className="h-52 overflow-y-auto p-2">
          <p className="text-xs text-muted-foreground mb-1.5">{CATEGORIES[activeCategory].name}</p>
          <div className="grid grid-cols-8 gap-0.5">
            {CATEGORIES[activeCategory].emojis.map((emoji) => (
              <button
                key={emoji}
                onClick={() => handleSelect(emoji)}
                className="w-8 h-8 flex items-center justify-center text-lg rounded hover:bg-secondary transition-colors"
              >
                {emoji}
              </button>
            ))}
          </div>
        </div>
      </PopoverContent>
    </Popover>
  );
}
